package org.eduran.certificacionjava2024.fase2.modelos;

public class Directores {
    private String run_dir;
    private String nom_dir;
    private String ape_dir;
    private String pro_dir;

    public Directores(){}

    public String getRun_dir() {
        return run_dir;
    }

    public void setRun_dir(String run_dir) {
        this.run_dir = run_dir;
    }

    public String getNom_dir() {
        return nom_dir;
    }

    public void setNom_dir(String nom_dir) {
        this.nom_dir = nom_dir;
    }

    public String getApe_dir() {
        return ape_dir;
    }

    public void setApe_dir(String ape_dir) {
        this.ape_dir = ape_dir;
    }

    public String getPro_dir() {
        return pro_dir;
    }

    public void setPro_dir(String pro_dir) {
        this.pro_dir = pro_dir;
    }
}
